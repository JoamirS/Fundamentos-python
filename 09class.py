'''
    Operadores aritiméticos
'''

# Declarando as variáveis pela primeira vez
VariableA = 11
VariableB = 4

# Soma
VariableSum = VariableA + VariableB
print('O resultado da soma das variáveis A e B é {}'.format(VariableSum))

# Subtração
VariableSubtraction = VariableA - VariableB
print('O resultado da subtração das variáveis A e B é {}'.format(VariableSubtraction))

# Multiplicação
VariableMultiplication = VariableA * VariableB
print('O resultado da multiplicação das variáveis A e B é {}'.format(VariableMultiplication))

# Divisão
VariableDivision = VariableA / VariableB
print('O resultado da divisão das variáveis A e B é {}'.format(VariableDivision))

# Resto da divisão
VariableModulo = VariableA % VariableB
print('O módulo da divisão das variáveis A e B é {}'.format(VariableModulo))

# ------------------------------------------------------------------------------------------------------------------

# Declarando as variáveis pela segunda vez
VariableC = 3
VariableD = 4
#                      | Base |   | Expoente |
VariablePotentiation = VariableC ** VariableD
print('O resultado de 3 elevado a 4 potência é {}'.format(VariablePotentiation))

